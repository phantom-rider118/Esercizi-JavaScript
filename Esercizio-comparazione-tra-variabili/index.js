// Confornto con il minore
console.log(10 < 11);
// Ugualianza devono avere lo stesso risultato ma non per forza lo stesso tipo di dato
console.log(45 == 45);
// ugualianza stretta deve essere uguale si nel tipo che nel risultato
console.log(true === true);
console.log(false === false);
// questa pur essendo un ugualianza si puo fare anche cosi perche true ha lo stesso risultato cioe 1
console.log(1 == true);
// deve essere minore o uguale e comprende il 4
console.log(4 <= 4);
// deve essere maggiore o uguale 
console.log(4 >= 3);